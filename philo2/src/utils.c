#include "philo.h"

int	ft_atoll(char *nbr)
{
	long long int			sum;
	int						i;
	int						n;

	i = 0;
	sum = 0;
	n = 0;
	while ((nbr[i] >= 9 && nbr[i] <= 13) || nbr[i] == ' ')
		i++;
	while(nbr[i] != '\0')
	{
		sum = sum * 10 + (nbr[i] - 48);
		i++;
	}
	return (sum);
}
